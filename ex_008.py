# Escreva um programa que leia um valor em metros e a
# exiba convertido em centimetros e milímetros.
metro = float(input('Quantos metros: '))
centimetros = float(metro * 100)
milimetros = float(metro * 1000)
print('São {} metros, centimetros {}, milimetros {}'.format(metro, centimetros, milimetros))


